let lastCompliment = "";

/**
 * Generates a toxic compliment using ONLY the local database.
 * No API calls are made to avoid quotas or external dependencies.
 */
export const generateToxicCompliment = async (category: ToxicCategory): Promise<string> => {
  // Simulate a bit of "thinking" time for UI feedback
  await new Promise(resolve => setTimeout(resolve, 800 + Math.random() * 400));
  
  const pool = TOXIC_DATABASE[category];
  let picked: string;
  
  // Pick a random compliment ensuring it's not the same as the last one
  do {
    picked = pool[Math.floor(Math.random() * pool.length)];
  } while (picked === lastCompliment && pool.length > 1);
  
  lastCompliment = picked;
  return picked;
};
